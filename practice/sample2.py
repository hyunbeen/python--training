class Calculator:
    def __init__(self,lista):
        self.sum = 0;
        self.lista = lista
    
    def sum(self):
        for i in len(self.lista):
            sum += self.lista[i]
        return sum;
    
    def avg(self):
        for i in len(self.lista):
            sum += self.lista[i]
        return sum/len(self.lista);

      
    
